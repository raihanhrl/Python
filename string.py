'''
String adalah satuan karakter
'''

print('===Penanda===')


# \ (untuk mencantumkan ')

print('hari jum\'at')

print('======')

# \\ (backslash)

print('satuan angka\\bilangan')

print('======')

# \t (tab)

print('biner = \t0 dan 1')

print('======')

# \b (backspace)

print('rudi dan radi \btya')

print('======')

# \r \n (newline)

print('koefisien a \nadalah 7')

print('\rkonstansta nya adalah 18x')

print('variabel nya \r\nadalah x')

print('======')

# raw string

print(r'C\Users:Raihan')

