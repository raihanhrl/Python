# Operasi Tambah
print("### Operasi Tambah ###")
a = 18
b = 3
hasil = a + b

print(a,'+',b,'=',hasil)

# Operasi Kurang
print("### Operasi Kurang ###")
c = 7
d = 9
hasil = c - d

print(c,'-',d,'=', hasil)

# Operasi Kali
print("### Operasi Kali ###")
e = 29
f = 2
hasil = e * f

print(e,'*',f,'=',hasil)

# Operas Bagi
print("### Operasi Bagi ###")
g = 32
h = 2
hasil = g / h

print(g,'/',h,'=',hasil)

# Operasi Eksponen (pangkat)
print("### Operasi Eksponen ###")
i = 40
j = 2
hasil = 40 ** 2

print(i,'***',j,'=',hasil)

# Operasi Modulus (Persen)
print("### Operasi Modulus ###")
k = 42
l = 2
hasil = 42 % 2

print(k,'%',l,'=',hasil)

# Operasi Floor Division (hasil bagi secara bulat)
print("### Operasi Floor Division ###")
m = 32
n = 2
hasil = m // n

print(m,'//',n,'=',hasil)